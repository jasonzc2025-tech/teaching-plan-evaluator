def get_activity_guide(activity_type: str) -> dict:
    guide = ACTIVITY_GUIDES.get(activity_type) or DEFAULT_GUIDE
    result = deepcopy(guide)
    result["activity_type"] = activity_type if activity_type in ACTIVITY_GUIDES else ""
    return result
